#+ Funciones / Variables definidas / Profundidad : [Depth : (04) ] 
#--------------------------------------------------------------------------------
#-- rmrf
#--------------------------------------------------------------------------------
#+ Funciones / Variables Requeridas
#--------------------------------------------------------------------------------
#-- 
#--------------------------------------------------------------------------------
#+ Informacion : 
#--------------------------------------------------------------------------------
#I Borra directorios de forma absoluta en la cadena de entrada en servidor
#--------------------------------------------------------------------------------
#- Sistema de Meta tags en scripts
#--------------------------------------------------------------------------------
#T delete folders files
#--------------------------------------------------------------------------------
#+ Parametros : 
#--------------------------------------------------------------------------------
#P ID [PJ_PATH]
#--------------------------------------------------------------------------------
#+ Ejemplo : 
#--------------------------------------------------------------------------------
#E rmrf aaa bb -- Borra los ficheros o carpetas aaa y bb
#-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-= 
rmrf()
{
VER="0.0.5"							;QP="Brqx Agile Architecture 2016"
FUNC="rmrf"

rmrf_NFO="Borra directorios de forma absoluta en la cadena de entrada en servidor  [$VER]($DEP) @${QP}"
rmrf_FMT="rmrf [FILES OR FOLDER LIST]"
rmrf_EXA="rmrf aaa bb"

if [ "${1}" == "-?" -o "${1}" == "" -o "${1}" == " " ] ; then
#==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--
#-- CABECERA MENSAJE COMPLETA
#--------------------------------------------------------------------------------
header_full "${rmrf_NFO}" "${rmrf_FMT}" "${rmrf_EXA}"
#--------------------------------------------------------------------------------
else

#Borra ficheros o directorios. No enlaces
for fich in "${@}" ; do
if [ "${fich}" != "/" -a  "${fich}" != "/root"  -a "${fich}" != "/etc" -a "${fich}" != "/usr" -a "${fich}" != " " -a "${fich}" != "*"  ] ; then
if [ -d ${fich} -o -f ${fich} ] ; then
rm -rf  "${fich}"						2> /dev/null
fi
fi
done

fi

}
