ckout()
{
VER="0.0.8"
FUNC="ckout"

ckout_NFO="Comprobacion puertos dominio [$VER]"
ckout_FMT="ckout [DOMAIN]"
ckout_EXA="ckout pbrqx.com"

ckout_OUT=0
ckout_ERR=""
ckout_MSG=""

#==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--
#-- CABECERA MENSAJE COMUN
#==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--
header_common "${ckout_NFO} [$VER]"
#==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--


if [ "${1}" == "-?" -o "${1}" == "" -o "${1}" == " " ] ; then

#==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--
#-- CABECERA MENSAJE ESPECIFICA
#--------------------------------------------------------------------------------
header_specific "${ckout_FMT}" "${ckout_EXA}"
#--------------------------------------------------------------------------------

else

SERVER=$1
PORT=$2

ck ${SERVER} ${PORT}

#==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--==--
#-- PIE DE ERROR MULTIPLE
#--------------------------------------------------------------------------------
multi_footer "${ck_OUT}" "${ck_NFO}" "${ck_MSG}" "${ck_ERR}"
#--------------------------------------------------------------------------------

fi

}
